export interface TilePolygon {
  Coords: number[];
  StackX: number;
  StackY: number;
}

export interface TileData {
  // IDs are now strings
  ID: string;
  Polygon: TilePolygon;
}

// Transform a TileData's Coords with scale + translation
export function transformTileData(
  tile: TileData,
  scaleX: number,
  scaleY: number,
  translateX: number,
  translateY: number
): TileData {
  const { Coords, StackX, StackY } = tile.Polygon;

  const transformedCoords: number[] = [];
  for (let i = 0; i < Coords.length; i += 2) {
    const x = Coords[i] * scaleX + translateX;
    const y = Coords[i + 1] * scaleY + translateY;
    transformedCoords.push(x, y);
  }

  const transformedStackX = StackX * scaleX + translateX;
  const transformedStackY = StackY * scaleY + translateY;

  return {
    ...tile,
    Polygon: {
      ...tile.Polygon,
      Coords: transformedCoords,
      StackX: transformedStackX,
      StackY: transformedStackY,
    },
  };
}


export function transformMapData(
  mapData: MapData,
  scaleX: number,
  scaleY: number,
  translateX: number,
  translateY: number
): MapData {
  return {
    ...mapData,
    Tiles: mapData.Tiles.map((tile) =>
      transformTileData(tile, scaleX, scaleY, translateX, translateY)
    ),
  };
}

// Union function: throws an error on ID collision
export function unionMapData(...maps: MapData[]): MapData {
  const unionTiles: TileData[] = [];
  const seenIDs = new Set<string>();
  let totalOffsetStacksX = 0;
  let totalOffsetStacksY = 0;

  for (const map of maps) {
    for (const tile of map.Tiles) {
      if (seenIDs.has(tile.ID)) {
        throw new Error(`ID collision: ${tile.ID}`);
      }
      seenIDs.add(tile.ID);
      unionTiles.push(tile);
    }
    totalOffsetStacksY += map.OffsetStacksY;
    totalOffsetStacksX += map.OffsetStacksX;
  }

  return {
    Tiles: unionTiles,
    OffsetStacksX: totalOffsetStacksX,
    OffsetStacksY: totalOffsetStacksY,
  };
}
